#include <stdio.h>
int main()
{
    int integerValue = 10;
    float floatValue = 3.14;
    char charValue = 'A';
    double doubleValue = 9.87654321;
    printf("Integer: %d\n", integerValue);
    printf("Float: %f\n", floatValue);
    printf("Character: %c\n", charValue);
    printf("Double: %lf\n", doubleValue);
    return 0;
}
